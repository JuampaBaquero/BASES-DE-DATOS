FROM PERSONA p
WHERE p.ID_PERSONA IN(
    SELECT ID_PERSONA
    FROM ESTUDIANTE)
AND
p.ID_PERSONA IN(
    SELECT ID_PERSONA
    FROM EXPOSITOR
);