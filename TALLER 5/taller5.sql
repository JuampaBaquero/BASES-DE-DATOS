    p.TIPODOC || ' ' || p.DOCUMENTO "DOCUMENTO", 
    p.NOMBRE || ' ' || p.APELLIDO "NOMBRES Y APELLIDOS"

FROM PERSONA P
    INNER JOIN EXPOSITOR e ON p.ID_PERSONA = e.ID_PERSONA
    INNER JOIN DIPLOMADO d ON e.ID_PERSONA = d.ID_PERSONA
WHERE d.NOMBRE = 'BLOCKCHAIN';

