CREATE TABLE PAIS
(
    NOMBRE_PAIS     VARCHAR2(70) NOT NULL,
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    PRIMARY KEY     (ID)
);

ALTER TABLE PAIS ADD CONSTRAINT NOMBRE_PAIS
    UNIQUE (NOMBRE_PAIS);

CREATE TABLE CIUDAD
(
    NOMBRE_CIUDAD   VARCHAR2(70),
    ID_PAIS         NUMBER(10, 0),
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY, --no se si cuneta como not null
    PRIMARY KEY     (ID)
);

ALTER TABLE CIUDAD ADD CONSTRAINT UQ_NAME 
    UNIQUE(NOMBRE_CIUDAD);

ALTER TABLE CIUDAD ADD CONSTRAINT FK_ID_PAIS
    FOREIGN KEY (ID_PAIS) REFERENCES PAIS;

CREATE TABLE ESTADIO
(
    NOMBRE_ESTADIO  VARCHAR2(70) NOT NULL,
    CAPACIDAD       INTEGER NOT NULL, 
    ID_CIUDAD       NUMBER(10, 0) NOT NULL,
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    PRIMARY KEY (ID)
);

ALTER TABLE ESTADIO ADD CONSTRAINT NOMBRE_ESTADIO
    UNIQUE (NOMBRE_ESTADIO);

ALTER TABLE ESTADIO ADD CONSTRAINT FK_ID_CIUDAD
    FOREIGN KEY (ID_CIUDAD) REFERENCES CIUDAD(ID);

CREATE TABLE CONFEDERACION
(
    NOMBRE_CONFEDERACION VARCHAR2(70), --Oooootro constraint
    ID NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    PRIMARY KEY (ID)
);

CREATE TABLE SELECCION
(
    NOMBRE_SELECCION    VARCHAR2(70), --Aquí va un constraint
    ID_CONFEDERACION    NUMBER(10, 0), --Aquí va una FK
    ID                  NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    PRIMARY KEY (ID)
);

CREATE TABLE PARTIDO
(
    ID_ESTADIO              NUMBER(10, 0), --FK
    FECHA                   DATE,
    FASE                    VARCHAR2(10),
    ID_SELECCION_LOCAL      NUMBER(10, 0), --FK
    ID_SELECCION_VISITANTE  NUMBER(10, 0), --FK
    GOLES_LOCAL             INTEGER,
    GOLES_VISITANTE         INTEGER,
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    PRIMARY KEY     (ID)
);