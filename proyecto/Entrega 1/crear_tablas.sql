CREATE TABLE PAIS
(
    NOMBRE_PAIS     VARCHAR2(70) NOT NULL,
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    CONSTRAINT PK_PAIS PRIMARY KEY (ID),
    CONSTRAINT UQ_NOMBRE_PAIS UNIQUE (NOMBRE_PAIS)
);

CREATE TABLE CIUDAD
(
    NOMBRE_CIUDAD   VARCHAR2(70) NOT NULL,
    ID_PAIS         NUMBER(10, 0) DEFAULT 0 NOT NULL,
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    CONSTRAINT PK_CIUDAD PRIMARY KEY (ID),
    CONSTRAINT CHK_ID_PAIS CHECK (ID_PAIS >= 0),
    CONSTRAINT UQ_NOMBRE_CIUDAD UNIQUE (NOMBRE_CIUDAD),
    CONSTRAINT FK_ID_PAIS FOREIGN KEY (ID_PAIS) REFERENCES PAIS(ID)
);

CREATE TABLE CONFEDERACION
(
    NOMBRE_CONFEDERACION VARCHAR2(70) NOT NULL,
    ID                   NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    CONSTRAINT PK_CONFEDERACION PRIMARY KEY (ID),
    CONSTRAINT UQ_NOMBRE_CONFEDERACION UNIQUE (NOMBRE_CONFEDERACION)
);

CREATE TABLE ESTADIO
(
    NOMBRE_ESTADIO  VARCHAR2(70) NOT NULL,
    CAPACIDAD       INTEGER DEFAULT 0 NOT NULL,
    ID_CIUDAD       NUMBER(10, 0) DEFAULT 0 NOT NULL,
    ID              NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,
    CONSTRAINT PK_ESTADIO PRIMARY KEY (ID),
    CONSTRAINT CHK_CAPACIDAD CHECK (CAPACIDAD >= 0),
    CONSTRAINT CHK_ID_CIUDAD CHECK (ID_CIUDAD >= 0),
    CONSTRAINT UQ_NOMBRE_ESTADIO UNIQUE (NOMBRE_ESTADIO),
    CONSTRAINT FK_ID_CIUDAD FOREIGN KEY (ID_CIUDAD) REFERENCES CIUDAD(ID)
);

CREATE TABLE SELECCION
(
    NOMBRE_SELECCION    VARCHAR2(70) NOT NULL,
    ID_CONFEDERACION    NUMBER(10, 0) DEFAULT 0 NOT NULL,
    ID                  NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,

    CONSTRAINT PK_SELECCION PRIMARY KEY (ID),
    CONSTRAINT CHK_ID_CONFEDERACION CHECK (ID_CONFEDERACION >= 0),
    CONSTRAINT UQ_NOMBRE_SELECCION UNIQUE (NOMBRE_SELECCION),
    CONSTRAINT FK_ID_CONFEDERACION FOREIGN KEY (ID_CONFEDERACION) REFERENCES CONFEDERACION(ID)
);

CREATE TABLE PARTIDO
(
    ID_ESTADIO              NUMBER(10, 0) DEFAULT 0 NOT NULL,
    FECHA                   DATE DEFAULT SYSDATE NOT NULL,
    FASE                    VARCHAR2(10) NOT NULL,
    ID_SELECCION_LOCAL      NUMBER(10, 0) DEFAULT 0 NOT NULL,
    ID_SELECCION_VISITANTE  NUMBER(10, 0) DEFAULT 0 NOT NULL,
    GOLES_LOCAL             INTEGER DEFAULT 0 NOT NULL,
    GOLES_VISITANTE         INTEGER DEFAULT 0 NOT NULL,
    ID                      NUMBER(10, 0) GENERATED BY DEFAULT AS IDENTITY,

    CONSTRAINT PK_PARTIDO PRIMARY KEY (ID),

    CONSTRAINT CHK_ID_ESTADIO CHECK (ID_ESTADIO >= 0),
    CONSTRAINT CHK_ID_SEL_LOCAL CHECK (ID_SELECCION_LOCAL >= 0),
    CONSTRAINT CHK_ID_SEL_VIS CHECK (ID_SELECCION_VISITANTE >= 0),
    CONSTRAINT CHK_GOLES_LOCAL CHECK (GOLES_LOCAL >= 0),
    CONSTRAINT CHK_GOLES_VIS CHECK (GOLES_VISITANTE >= 0),
    CONSTRAINT CHK_FASE CHECK (FASE IN ('GRUPOS', 'OCTAVOS', 'CUARTOS', 'SEMIFINAL', 'FINAL')),
    CONSTRAINT CHK_JUGAR_SI_MISMA CHECK (ID_SELECCION_LOCAL != ID_SELECCION_VISITANTE),

    CONSTRAINT FK_ID_ESTADIO FOREIGN KEY (ID_ESTADIO) REFERENCES ESTADIO(ID),
    CONSTRAINT FK_ID_SEL_LOCAL FOREIGN KEY (ID_SELECCION_LOCAL) REFERENCES SELECCION(ID),
    CONSTRAINT FK_ID_SEL_VIS FOREIGN KEY (ID_SELECCION_VISITANTE) REFERENCES SELECCION(ID)
);